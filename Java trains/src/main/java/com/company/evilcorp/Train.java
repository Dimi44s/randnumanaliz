package com.company.evilcorp;

class Train {
    public int Priority;
    public int Weight;
    public int Length;
    public int LeadingAxes;
    public int NumberOfTrain;

    public Train(int priority, int weight, int length, int leadingAxes, int numberOfTrain) {
        this.Priority = priority;
        this.Weight = weight;
        this.Length = length;
        this.LeadingAxes = leadingAxes;
        this.NumberOfTrain = numberOfTrain;
    }
}
